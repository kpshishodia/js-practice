// VARIABLES EXAMPLE

// var
var a = 10;
var a = 20; // redeclaration allowed
a = 30;     // reassignment allowed
console.log("var:", a);

// let
let b = 10;
// let b = 20; // ❌ redeclaration not allowed
b = 30;       // reassignment allowed
console.log("let:", b);

// const
const c = 10;
// const c = 20; // ❌ redeclaration not allowed
// c = 30;       // ❌ reassignment not allowed
console.log("const:", c);

